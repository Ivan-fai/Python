def search():
    unique = list()
    A = input("Введіть слово з латинських літер: ")
    B = list(A)

    print("Введене слово: ",A)
    for char in B:
        if char not in unique:#якщо символ зустрічаєтсья перший раз, то він вноситься в список
            unique.append(char)
    print("Перші входження літер, якы збергыають порядок входження:")
    print(" ".join(unique))#вивід був оформлений списком, бо якщо зробити множиною, то порядок символів зіб'ється
search()